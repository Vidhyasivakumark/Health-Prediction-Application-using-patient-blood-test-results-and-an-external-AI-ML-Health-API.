
import sqlite3

# =====================================================
# CONNECT DATABASE
# =====================================================

conn = sqlite3.connect("health_app.db")

cursor = conn.cursor()

# =====================================================
# FETCH DATA
# =====================================================

cursor.execute("SELECT * FROM patient_records")

rows = cursor.fetchall()

# =====================================================
# DISPLAY DATA
# =====================================================

for row in rows:

    print(row)

# =====================================================
# CLOSE DATABASE
# =====================================================

conn.close()

